#include <iostream>

// ASSIGNMENT
int main(){

    // Initialize with bracelet
    int num1 {22};

    std::cout << num1 << std::endl;

    // Re-assign num1 with Assignment
    num1 = 44;

    std::cout << num1 << std::endl;




    // BASIC OPERATION   # -------------------------------------- #


    int number1 {10};
    int number2 {4};

    // ADDITION
    int result = number1 + number2;
    std::cout << result << std::endl;

    // SUBTRACTION
    result = number1 - number2;
    std::cout << result << std::endl;

    // MULTIPLICATION
    result = number1 * number2;
    std::cout << result << std::endl;

    // DIVISION
    result = number1 / number2;
    std::cout << result << std::endl;

    // MODULUS
    result = number1 % number2;
    std::cout << result << std::endl;

    return 0;
}
 